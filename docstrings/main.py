def soma(x=0, y=0, z=0):
    """
    Retorna o somatóio de até 3 valores números quaisquer.
    Todos os parâmetros são opcionais.

    :param x: valor número (opcional)
    :param y: valor número (opcional)
    :param z: valor número (opcional)
    """
    return x+y+z

